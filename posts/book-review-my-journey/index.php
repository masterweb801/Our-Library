<h3>My Journey</h3><br>
<h4>A.P.J. Abdul Kalam Azad</h4><br><br>
<p>Abdul Kalam’s story is one that fascinates 
all. A young boy from Rameshwaram who becomes 
one of India’s most renowned scientists, apart 
from holding the highest post the country can 
offer. And to top it off, there is calmness 
and serenity in Kalam’s demeanour that makes 
everyone respect him.</p>
<p>In this book, Kalam takes a more personal 
side as he tells us all the things that helped 
him become what he is today. He offers not only 
his own personal stories, but also inspirational 
stories about how he beat the odds and survived 
the difficulties. `My Journey’ narrates the human 
side of Dr Kalam, as a boy, son, student, scientist, 
excelling despite severe struggles, and ultimately 
blossoming as a great human being, top class scientist 
and India’s first citizen.</p>
<p>The book has several chapters, each with a 
particular theme. Each theme is one aspect that 
influenced Kalam. These themes range from family, 
friends, failures, mentors, different memories and 
so on. In the first few chapters, he talks about the 
impact his close-knit family had on him and how he 
managed to be affected by a personal quality of each 
family member, mainly: his father’s serenity, his 
mother’s unconditional love, his sister’s encouragement 
and her husband, Ahmed Jalalludin’s wisdom. He also 
talks about how the peaceful co-existence of the three 
religions: Hinduism, Islam and Christianity in his 
village. The book deals greatly with Kalam’s scientific 
life and not so much with his presidential life. He 
writes in length about his experiments in rocket 
sciences and how he failed to become a pilot, his 
childhood dream. That is outlined beautifully in 
the chapter “When I Failed”. There is a chapter 
that describes the profound impact Dr. Vikram 
Sarabhai has had on him. The most touching part 
of the book is, of course when Kalam describes 
his parents’ and Jalalludin’s deaths.</p>
<p>The book is written simply, with short, 
crisp and precise sentences. There is a clear 
flow of thought in the book, which ensures that 
it doesn’t digress. The language is unpretentious 
and to the point. Kalam doesn’t shoot for literary 
elegance. This is the beauty of the book. You see 
the each point and the reason for it. The book, 
full of anecdotes and experiences is rich and 
motivating. Since each chapter has a theme, it 
feels like separate stories. But they are connected 
by a single thread: Kalam.</p>
<p>This book reflects the qualities of Kalam 
that we admire: simplicity, integrity and honesty. 
There is an issue of repeating points, but that is 
inevitable in autobiographical books. This is a 
good book that I would recommend for all.</p><br>

