<h3>Wings Of Fire</h3><br>
<h4>A.P.J. Abdul Kalam Azad</h4><br><br>
<p>“Wings of Fire: An Autobiography of Abdul 
Kalam”  is written by Dr. APJ Abdul Kalam and 
Arun Tiwari. This book is an autobiography of 
Dr. APJ Abdul Kalam, former president of India. 
This story sheds light on the journey of a young 
boy from Rameswaram to become a renowned scientist. 
It reflects how simple living, dedication, strong 
will and hard work led to success. It also shows 
how cultural unity impacts the lives of individuals.</p>

<p>The story begins with Kalam as a boy  around 
1930 in the village of Rameswaram where he lived 
with his parents and four brothers and one sister. 
In this book, the author tells us how the role of 
family, relatives, friends, and society contribute 
to an individual’s life. Kalam describes his village 
where everyone lives in harmony. Religious discrimination 
was unknown at that time. A passage that shows the 
closeness  they had: "<b>The high priest of Rameswaram 
temple, Pakshi Lakshmana Sastry, was a very close friend 
of my father’s. One of the most vivid memories of my 
early childhood is of the two men, each in his traditional 
attire, discussing spiritual matters</b>"</p>

<p>When he was very young, Kalam wanted to join 
the Indian Airforce but he was not able to clear 
the interview. Far from being demotivated, he focused 
more on studies, started his career in Aeronautical 
Development Establishment (ADE), and was involved in 
the design of a hovercraft. He went to NASA on a training 
program. He moved to Indian Space Research and there 
he met Vikram Sarabhai. He also met many geniuses who 
inspired him. The book also describes leadership and 
teamwork, while making the projects. It shows how a  
man becomes a leader whenever there is a successful 
project or a failed one.</p>

<p>The book reveals extensive and detailed project 
work by Dr. APJ Kalam, which I find distracting from 
the story of his life. Some readers may find this 
detailed descriptions boring and unappealing. It 
highlights the successful execution of “Agni” and 
other projects.</p>

<p>The book narrates India’s technological achievements 
at a time, few nations can boast of. It speaks about 
some brilliant minds who worked behind Indian space 
research such as Vikram Sarabhai and Dr. Brahm Prakash 
and their contribution towards ISRO.</p>

<p>As a reader, this book initially is very interesting 
particularly the portions from his younger days to a college 
student like how his family encouraged him. Later, the writing 
becomes a bit monotonous as it gives minute illustrations of 
the projects. At the end, he offers advice to the younger 
generation not to get demotivated and lead a positive life.</p>

<p>This book demonstrates how Dr. APJ Abdul Kalam’s simple 
living and positive thinking became an inspiration for the 
Indian youth.</p>

